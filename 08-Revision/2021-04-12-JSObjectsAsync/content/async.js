// Async vs Sync

 /* 
 - In synchronous programming, tasks happen one at a time. When you call a function that performs a long-running task, it return only when the task has finished and can return the result.
 This stops your program for the time the action takes. 
 - In asynchronous programming, multiple tasks happen at the same time. When you start a task, your program continues to run. When the task has finished, the program is informed and gets access to the results of the task.
 

 - In javascript, asynchronoush programming is relatively new and still catching up.
 - This means, Javascript will evolve with asynchronous functionality in the time to come.
 */

 