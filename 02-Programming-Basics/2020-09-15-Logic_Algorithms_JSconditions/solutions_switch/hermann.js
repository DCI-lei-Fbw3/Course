var fruit = prompt("Enter a fruit's name", "fruit's name");
switch(fruit){
  case "apples":
    console.log("Apples are ok.");
    break;
    case "bananas":
    console.log("Bananas are good before and after a football match.");
    break;
    case "oranges":
    console.log("I can eat oranges all day!");
    break;
    case "mangoes":
    console.log("Did someone say mangoes? Where?");
    break;
    default:
      console.log("Enter one of the following fruit's name : apples, bananas, oranges, mangoes");
}